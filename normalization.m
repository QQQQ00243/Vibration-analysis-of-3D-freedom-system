clear;

A = [4.5955, 6.0548, 3.4000;
    6.0548, 9.8865, 6.0548;
    3.4000, 6.0548, 4.5955];
A = A .* 1e-6;
K = inv(A);
M = diag([0.76, 0.79, 0.76]);

[U, D] = eig(M\K);
% Matlab中默认排列顺序为从左到右阶数降序排列，
% 这里交换第一、三列，则从左到右阶数由低到高
U(:, [1, 3]) = U(:, [3, 1]);

V = zeros(size(U)); % 提前为正则矩阵V分配内存
Mr = U'*M*U;
for i = 1:size(U, 1)
    V(:, i) = U(:, i) / sqrt(Mr(i, i));
end


